    void calculateDisc(){
        try{
        String dp=txtdiscp.getText();
        double dis;
        if(dp.equals("")){
            dis=0;
            //txtdiscp.setText("0");
        }else{
            dis=Double.parseDouble(dp);
        }
        double tamt=Double.parseDouble(txttotalamt.getText());
        double disamt=tamt*(dis/100);
        txtdisc.setText(disamt+"");
        double netamt=tamt-disamt;
        txtnetamt.setText(netamt+"");
        }catch(Exception ex){}
    }
