    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // Add
        String itemid=txtid.getText();
        String itemname=txtname.getText();
        String qty=txtqty.getText();
        String unit=txtunit.getText();
        String price=txtprice.getText();        
        if(qty.equals("")){
            Messages.showError("Qty cannot be left empty..", "Input Error");
            txtqty.requestFocus();
            return;
        }
        if(!Rules.isInteger(qty)){
            Messages.showError("Qty should be in Integer format..", "Input Error");
            txtqty.requestFocus();
            return;
        }
        if(unit.equals("")){
            Messages.showError("Unit cannot be left empty..", "Input Error");
            txtunit.requestFocus();
            return;
        }
        DefaultTableModel dt=(DefaultTableModel)tab1.getModel();
        double total= Integer.parseInt(txtqty.getText())* Double.parseDouble(txtprice.getText());        
        dt.addRow(new Object[]{txtid.getText(),txtname.getText(),txtqty.getText(),txtunit.getText(),txtprice.getText(),total});
        calculateTotal();
        txtid.requestFocus();
        
    }//GEN-LAST:event_btnAddActionPerformed
    void calculateTotal(){
        double t=0;
        for(int i=0;i<tab1.getRowCount();i++){
            t=t+Double.parseDouble(tab1.getValueAt(i, 5)+"");
        }
        txttotalamt.setText(t+"");
        calculateDisc();
    }
    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        //Save
        String billno=txtbno.getText();
        if(txtdate.getText().trim().equals("")){
            Messages.showError("Bill Date cannot be left empty..", "Input Error");
            txtdate.requestFocus();
            return;
        }
        String billdate=txtdate.getDate().format(DateTimeFormatter.ISO_DATE);                
        String customer=txtcname.getText();
        String tamt=txttotalamt.getText();
        String discp=txtdiscp.getText();
        String discamt=txtdisc.getText();
        String namt=txtnetamt.getText();
        if(customer.equals("")){
            Messages.showError("Customer cannot be left empty..", "Input Error");
            txtcname.requestFocus();
            return;
        }
        if(discp.equals("")){
            Messages.showError("Discount Percent cannot be left empty..", "Input Error");
            txtdiscp.requestFocus();
            return;
        }
        if(!Rules.isDouble(discp)){
            Messages.showError("Discount Percent should be in number format..", "Input Error");
            txtdiscp.requestFocus();
            return;
        }
        //Master Record Entry
        db.execute("Insert into Bill values (?,?,?,?,?,?,?)", billno,billdate,customer,tamt,discp,discamt,namt);
        
        //Details Entry
        for(int i=0;i<tab1.getRowCount();i++){
            String itemno=tab1.getValueAt(i, 0).toString();
            String itemname=tab1.getValueAt(i, 1).toString();
            String qty=tab1.getValueAt(i, 2).toString();
            String unit=tab1.getValueAt(i, 3).toString();
            String price=tab1.getValueAt(i, 4).toString();
            String total=tab1.getValueAt(i, 5).toString();
            db.execute("Insert into BillItems values (?,?,?,?,?,?,?)", billno,itemno,itemname,qty,unit,price,total);
            //Update Stock in item table
            db.execute("Update Product set Qty=Qty-? where ProductNo=?", qty,itemno);            
        }
        db.updateID("BillNo");
        txtbno.setText(db.getID("BillNo"));
        if(parent!=null)
        db.fillTable(parent.table1, "select * from Bill");
        Messages.showInfo("Sales Bill Generated Successfully..", "Success");                

    }//GEN-LAST:event_btnSaveActionPerformed

